package vn.edu.hust.soict.soe.assetmanagement.asset.enums;

/**
 * Enumeration for asset depreciation methods.
 * STRAIGHT_LINE: Depreciation is the same each year.
 * DECLINING_BALANCE: Higher depreciation in earlier years, decreasing over time.
 */

public enum DepreciationMethod {
    STRAIGHT_LINE,     
    DECLINING_BALANCE
}